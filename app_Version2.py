def spawn_yeep(yeep_type, count=1):
    """Spawn yeeps in the game"""
    yeep_types = ["basic", "aggressive", "passive", "flying", "rare"]
    
    if yeep_type.lower() not in yeep_types:
        print(f"\033[91m" + f"Unknown yeep type: {yeep_type}. Available: {', '.join(yeep_types)}" + "\033[0m\n")
        return
    
    try:
        count = int(count)
        if count <= 0:
            print("\033[91m" + "Count must be greater than 0" + "\033[0m\n")
            return
    except ValueError:
        print("\033[91m" + "Count must be a valid number" + "\033[0m\n")
