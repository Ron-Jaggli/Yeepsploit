def server_command(action):
    """Server/multiplayer management"""
    if action.lower() == "start":
        game_state["server_running"] = True
        print(f"\033[92m" + "[+] Server started" + "\033[0m\n")
    elif action.lower() == "stop":
        game_state["server_running"] = False
        game_state["connected_players"] = []
        print(f"\033[92m" + "[+] Server stopped" + "\033[0m\n")
    elif action.lower() == "status":
        status = "running" if game_state["server_running"] else "stopped"
        print(f"\n[*] Server Status: {status}")
        print(f"[*] Connected Players: {len(game_state['connected_players'])}\n")
    else:
        print("\033[91m" + "Invalid action. Use: start, stop, or status" + "\033[0m\n")

def kick_player(player):
    """Kick a player from the server"""
    if not game_state["server_running"]:
        print("\033[91m" + "Server is not running" + "\033[0m\n")
        return
    
    for p in game_state["connected_players"]:
        if p["id"] == player or p["name"].lower() == player.lower():
            game_state["connected_players"].remove(p)
            print(f"\033[92m" + f"[+] Kicked player: {p['name']}" + "\033[0m\n")
            return
    
    print(f"\033[91m" + f"Player not found: {player}" + "\033[0m\n")

